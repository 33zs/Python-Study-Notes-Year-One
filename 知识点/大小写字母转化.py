#1 转化为小写
#str.lower()
print("ABC".lower())
#2 转化为大写
#str.upper()
print("abc".upper())
#3 将第一个字母转化为大写，其余为小写
#str.capitalize()
print("aABC".capitalize())
#4 首字母转为大写
#str.title()
print("abc".title())
#5 将大写字母转为小写，小写字母转为大写
#str.swapcase()
print("Abc".swapcase())
#6 将所有大写字母转为小写
#str.casefold()
print("Abc".casefold())